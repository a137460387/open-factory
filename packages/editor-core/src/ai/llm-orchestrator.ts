/**
 * Validate that an instruction's target references are within bounds.
 */
export function validateInstructionTarget(
  instruction: EditInstruction,
  materialCount: number,
  maxDurationSec: number
): { valid: boolean; error?: string } {
  const { target } = instruction;

  if (target.materialIndex !== undefined) {
    if (target.materialIndex < 0 || target.materialIndex >= materialCount) {
      return { valid: false, error: `materialIndex ${target.materialIndex} out of range [0, ${materialCount - 1}]` };
    }
  }

  if (target.startSec !== undefined && target.startSec < 0) {
    return { valid: false, error: `startSec ${target.startSec} is negative` };
  }

  if (target.endSec !== undefined && target.endSec > maxDurationSec) {
    return { valid: false, error: `endSec ${target.endSec} exceeds max duration ${maxDurationSec}` };
  }

  if (target.startSec !== undefined && target.endSec !== undefined && target.startSec >= target.endSec) {
    return { valid: false, error: 'startSec must be less than endSec' };
  }

  return { valid: true };
}

/**
 * Sort instructions by execution priority.
 * Some actions must run before others (e.g., cut before reorder).
 */
export function sortInstructionsByPriority(instructions: EditInstruction[]): EditInstruction[] {
  const priority: Record<EditActionType, number> = {
    split: 0,
    cut: 1,
    trim: 2,
    merge: 3,
    reorder: 4,
    speed: 5,
    fade: 6,
    add_transition: 7,
    add_effect: 8,
    adjust_audio: 9,
    add_subtitle: 10,
    narration: 11,
  };

  return [...instructions].sort((a, b) => {
    const pa = priority[a.action] ?? 99;
    const pb = priority[b.action] ?? 99;
    return pa - pb;
  });
}

// ─── LLM Provider Interface ────────────────────────────────────

/** Abstract LLM provider interface for testability */
export interface LLMProviderAdapter {
  /** Send messages and get response */
  chat(messages: LLMMessage[], config: LLMRequestConfig): Promise<LLMResponse>;
}

/**
 * Build headers for LLM API call.
 */
export function buildLLMHeaders(provider: AIProvider): Record<string, string> {
  const headers: Record<string, string> = {
    'Content-Type': 'application/json',
  };

  if (provider.apiKey) {
    headers['Authorization'] = `Bearer ${provider.apiKey}`;
  }

  if (provider.customHeaders) {
    Object.assign(headers, provider.customHeaders);
  }

  return headers;
}

/**
 * Build the request body for OpenAI-compatible API.
 */
export function buildOpenAIRequestBody(
  messages: LLMMessage[],
  config: LLMRequestConfig
): Record<string, unknown> {
  const openaiMessages = messages.map(msg => {
    const parts: Array<{ type: string; text?: string; image_url?: { url: string } }> = [];

    parts.push({ type: 'text', text: msg.content });

    if (msg.images) {
      for (const img of msg.images) {
        parts.push({
          type: 'image_url',
          image_url: { url: `data:${img.mimeType};base64,${img.base64}` },
        });
      }
    }

    return {
      role: msg.role,
      content: parts.length === 1 && parts[0].type === 'text' ? msg.content : parts,
    };
  });

  return {
    model: config.model ?? config.provider.defaultModel,
    messages: openaiMessages,
    max_tokens: config.maxTokens ?? 4096,
    temperature: config.temperature ?? 0.7,
    ...(config.responseFormat === 'json' ? { response_format: { type: 'json_object' } } : {}),
  };
}

// ─── Orchestrator ───────────────────────────────────────────────

export interface OrchestratorConfig {
  /** LLM provider adapter */
  adapter: LLMProviderAdapter;
  /** Provider info */
  provider: AIProvider;
  /** Model override */
  model?: string;
  /** Max retries on parse failure */
  maxRetries?: number;
  /** Filter instructions below this confidence */
  confidenceThreshold?: number;
}

const DEFAULT_CONFIDENCE_THRESHOLD = 0.6;

/**
 * Generate an editing plan from materials and user request.
 */
export async function generateEditPlan(
  materials: MaterialMetadata[],
  userRequest: string,
  config: OrchestratorConfig
): Promise<{ plan: EditPlan | null; response: LLMResponse; parseErrors?: ParseError[] }> {
  const messages = buildEditingPrompt(materials, userRequest);
  const requestConfig: LLMRequestConfig = {
    provider: config.provider,
    model: config.model,
    responseFormat: 'json',
    temperature: 0.7,
  };

  const maxRetries = config.maxRetries ?? 2;
  let lastResponse: LLMResponse | null = null;
  let lastErrors: ParseError[] | undefined;

  for (let attempt = 0; attempt <= maxRetries; attempt++) {
    const response = await config.adapter.chat(messages, requestConfig);
    lastResponse = response;

    const result = parseEditPlan(response.content);
    if (result.ok) {
      // Filter by confidence
      const threshold = config.confidenceThreshold ?? DEFAULT_CONFIDENCE_THRESHOLD;
      const filtered = {
        ...result.data,
        instructions: result.data.instructions.filter(i => i.confidence >= threshold),
      };
      return { plan: filtered, response };
    }

    lastErrors = result.errors;
  }

  return { plan: null, response: lastResponse!, parseErrors: lastErrors };
}

/**
 * Generate platform-specific content for video publishing.
 */
export async function generatePlatformContent(
  metadata: MaterialMetadata,
  platforms: string[],
  config: OrchestratorConfig,
  userHints?: string
): Promise<{ contents: PlatformContent[] | null; response: LLMResponse; parseErrors?: ParseError[] }> {
  const messages = buildPlatformPrompt(metadata, platforms, userHints);
  const requestConfig: LLMRequestConfig = {
    provider: config.provider,
    model: config.model,
    responseFormat: 'json',
    temperature: 0.8,
  };

  const response = await config.adapter.chat(messages, requestConfig);
  const result = parsePlatformContent(response.content);

  if (result.ok) {
    return { contents: result.data, response };
  }

  return { contents: null, response, parseErrors: result.errors };
}
